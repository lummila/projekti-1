# Instructions
# 1. Welcome to play chase the rat game! You'll need to enter an existing username and a PIN-code to play with your user
# OR if you are a new player you can create your own username and a four-digit PIN-code.
# In this game you'll travel between different airports, trying to find 'The rat' who owns you money.
# The rat has done some airport-hopping and he has travelled somewhere in Europe.
# The game will give you clues of The rat's route and his final location.
# The game will draw a new four pit-stop route and a final destination for The rat each round.
# Your goal is to follow the given clues and try to find him. There are in total of ten rounds in each game for you to try to find The rat.
# You'll need to solve the clues and follow the route that they guide.
# You'll have to take in consideration your emissions while flying,
# and you have a limited amount of money to spend on your trip.
# The game will start by telling you your first clue and after solving it you can start travelling to the first pit-stop.
# If you get the first clue correct, and travel to the right airport, the game will give you a clue of The rat's final destination.
# If you don't solve the first clue, you'll have to wait one round at the wrong airport.
# With the clues, you also have chance to be given a coincidence. The coincidences can either have a positive OR negative impact on you.
# But remember: if you solve the clue the possibility to get a positive coincidence is much higher
# AND if you travel to the wrong airport you are more likely to be drawn a negative coincidence.
# Each time you travel you'll use one round and if you travel to a wrong location, you'll have to wait one extra round.
# If you reach the final destination and find The rat within the given rounds, and you haven't used up all your money: You'll win.
# After reaching the goal the game will calculate your final points by summing up
# how many rounds you used, your emissions and the money that's left.
# If you don't find The rat within the ten rounds, or you don't have any money left: you'll lose.

